export const getNearbyLandmarks = (lat: number, lng: number, radiusKm: number = 10): ARLandmark[] => {
  return landmarks.map(landmark => {
    const distance = calculateDistance(lat, lng, landmark.coordinates.lat, landmark.coordinates.lng);
    const bearing = calculateBearing(lat, lng, landmark.coordinates.lat, landmark.coordinates.lng);
    return { ...landmark, distance, bearing };
  }).filter(l => (l.distance ?? 0) <= radiusKm).sort((a, b) => (a.distance ?? 0) - (b.distance ?? 0));
};

const calculateDistance = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const R = 6371;
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
};

const calculateBearing = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const lat1Rad = lat1 * Math.PI / 180;
  const lat2Rad = lat2 * Math.PI / 180;
  const y = Math.sin(dLng) * Math.cos(lat2Rad);
  const x = Math.cos(lat1Rad) * Math.sin(lat2Rad) - Math.sin(lat1Rad) * Math.cos(lat2Rad) * Math.cos(dLng);
  const bearing = Math.atan2(y, x) * 180 / Math.PI;
  return (bearing + 360) % 360;
};
