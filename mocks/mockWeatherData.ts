const createCondition = (
  type: WeatherConditionType,
  temp: number,
  humidity: number,
  wind: number,
  uv: number,
  precip: number,
  description: string
): WeatherCondition => ({
  type,
  temperature: temp,
  feelsLike: temp - (wind > 20 ? 3 : 0) + (humidity > 80 ? 2 : 0),
  humidity,
  windSpeed: wind,
  windDirection: 'NE',
  uvIndex: uv,
  visibility: type === 'fog' ? 2 : type === 'mist' ? 5 : 10,
  precipitation: precip,
  cloudCover: type === 'sunny' ? 10 : type === 'partly_cloudy' ? 40 : 80,
  pressure: 1013,
  description,
  icon: type,
});

